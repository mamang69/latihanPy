# operasi aritmatika

a = 10
b = 3

# operasi tambah

# operasi pertambahan
hasil = a+b
print(a, "+", b, " = ", hasil)

# operasi pengurangan
hasil = a-b
print(a, "-", b, " = ", hasil)

# operasi perkalian
hasil = a*b
print(a, "x", b, " = ", hasil)

# operasi pembagian
hasil = a/b
print(a, ":", b, " = ", hasil)

# operasi eksponen(perpangkatan) **
hasil = a**b
print(a, "^", b, " = ", hasil)

# operasi mmodulus %
hasil = a % b
print(a, "mod", b, " = ", hasil)

# operasi floor division(pembulatan hasil bagi(kebawah)) //)
hasil = a // b
print(a, "//", b, " = ", hasil)

# prioritas operasi di PY
'''
    1. ()
    2. eksponen
    3. kali,bagi,mod,floor
    4. penjumlahan dan pengurangan
'''
